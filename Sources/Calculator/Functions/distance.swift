/// Computes the distance between the arguments.
///
/// - Parameters:
///   - x: a float argument.
///   - y: a float argument.
/// - Returns: the distance between the arguments.
public func distance(_ x: Float, _ y: Float) -> Float { abs(x - y) }

/// Computes the distance between the arguments.
///
/// - Parameters:
///   - x: a double argument.
///   - y: a double argument.
/// - Returns: the distance between the arguments.
public func distance(_ x: Double, _ y: Double) -> Double { abs(x - y) }
